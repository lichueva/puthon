# Введення рядка користувачем
input_string = input("Введіть довільний рядок: ")

# Перевірка, чи довжина рядка дозволяє виконати операцію зрізу
if len(input_string) >= 7:
    # Отримання кожного 7-го символу з початку до кінця
    result = input_string[6::7]
    print("Результат:", result)
else:
    print("Довжина рядка недостатня для виконання операції зрізу.")
