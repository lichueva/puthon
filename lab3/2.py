# Введення чотиризначного числа від користувача
n = int(input("Введіть чотиризначне число: "))

# Перевірка, чи введене число є чотиризначним
if 1000 <= n <= 9999:
    # Початковий лічильник кількості цифр 7
    count = 0

    # Перетворення числа на рядок для обробки символів
    n_str = str(n)

    # Перевірка кожного символу у рядку
    for digit in n_str:
        if digit == '7':
            count += 1

    # Виведення результату
    print(f"Цифра 7 зустрічається {count} раз(и) у числі {n}.")
else:
    print("Введене число не є чотиризначним.")
